/**
 * Class for convertto while loop.
 */
public class ConverttoWhileLoop {
	/**
	 * { function_description }
	 *
	 * @param      args  The arguments
	 */
	public static void main(String[] args) {
		int l = 0;
		while (l<10) {
			l+=2;
			System.out.println(l);
		}
		System.out.println("GoodBye");
	}
}
